/** what is node? **/
// Node.js is a JavaScript run-time that uses the v8 engine.
// Node.js came into existence when the original developers of JavaScript extended it from something you could only run
// in the browser to something you could run on your machine as a standalone application.
// Both your browser JavaScript and Node.js run on the V8 JavaScript runtime engine. This engine takes your JavaScript
// code and converts it into a faster machine code (because we don't need to interpret it. Machine code is low level
// code which the computer can run without needing to first interpret it.

// V8 is an open source runtime engine written in C++ - takes js code and compiles it to a machine code.
// JavaScript -> V8(C++) -> Machine Code
// V8 implements a script called ECMAScript as specified in ECMA-262. ECMAScript was created by Ecma International to
// standardize JavaScript.

// javascript to node
// window => global
// document => process

// Node.js
// (1) uses an event-driven,
// (2) non-blocking I/O model that makes it lightweight and efficient.

// I/O - reading/writing to a DB, communication to IOTs, http requests, working with files.

